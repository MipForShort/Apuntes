#La tupla puede ser una función en base a los datos que le demos a empaquetar
tupla = tuple(["dato1", "dato2"])
print(tupla)

#Una forma de crear tuplas es escribiendo el nombre de la variable para después
#mediante comillas asignar datos que se separan mediante una coma
tupla = "dato1.5", "dato2.5"
print(tupla)

#Creando una tupla sin paréntesis de un sólo dato
#Observa que se define poniendo una coma al final del único dato
tupla = "dato1.5.1",
print(type(tupla), "\n", tupla)