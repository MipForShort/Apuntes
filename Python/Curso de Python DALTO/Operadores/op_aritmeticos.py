#Suma y resta (+ y -)
suma = 2+2
print(suma)

resta = 4-2
print(resta)

#Multiplicación y división (* y /)
#La división devuelve siempre un float

multi = 2*3
print(multi)

division = 6/3
print(division)

#Potenciación (**)
exponente = 5**2
print(exponente)

#División baja (//)
#Devuelve enteros redondeado hacia abajo
division_baja = 12//2
print(division_baja)

#Resto o módulo
resto = 12%2
print(resto)

#Como saber los tipos de datos

#Int
tipo_de_dato = type(5)
print(tipo_de_dato)

#String
tipo_de_dato = type("Mip")
print(tipo_de_dato)

#Booleano
tipo_de_dato = type(True)
print(tipo_de_dato)

#Float
tipo_de_dato = type(4.3)
print(tipo_de_dato)

#Lista
tipo_de_dato = type(["Mario", "Mip", 4.5, 5, True])
print(tipo_de_dato)

#Tupla
tipo_de_dato = type(("Mario", "Mip", 4.5, 5, True))
print(tipo_de_dato)

#Conjunto
tipo_de_dato = type({"Mario", "Mip", 4.5, 5, True})
print(tipo_de_dato)